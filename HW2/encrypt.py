def f_Func(_rInput, _ikey):

    # expansion _rInput 32b -> 48b (E)
    tmp = ""
    for i in E:
        tmp = tmp + _rInput[i-1]

    # xor with round key
    tmp = '{0:b}'.format(int(tmp, 2) ^ int(_ikey, 2)).zfill(48)

    # S-box substition (S)
    result = ""
    for i in range(0, 8):
        splitTmp = tmp[i*6:i*6+6]
        buf = (splitTmp[0]+splitTmp[-1])
        row = int(buf, 2)
        col = int(splitTmp[1:5], 2)
        Si = S[i]
        result = result + '{0:b}'.format(Si[row][col]).zfill(4)

    # permutation (P)
    output = ""
    for i in P:
        output = output+result[i-1]

    return output


def encrypt(_left, _right, _ikey):
    # tmp = f ( Ri , ikey)
    tmp = f_Func(_right, _ikey)
    # xor ( Li , tmp )
    tmp = '{0:b}'.format(int(_left, 2) ^ int(tmp, 2)).zfill(32)
    # return result
    return (_right, tmp)


def keySchedule(_key):
    roundKey = []
    # permutation (PC_1) 56bits
    key = ""
    for i in PC_1:
        key = key + _key[i-1]
    # loop (0~16) times
    ci = key[:28]
    di = key[28:]
    for i in range(0, 16):

        # if i=0,1,8,15 => 1b;
        if i == 0 or i == 1 or i == 8 or i == 15:
            ci = ci[1:]+ci[0]
            di = di[1:]+di[0]

        # else => 2b
        else:
            ci = ci[2:]+ci[0:2]
            di = di[2:]+di[0:2]

        # permutation (PC_2)
        tmp = ci+di
        tmpKey = ""
        for j in PC_2:
            tmpKey = tmpKey + tmp[j-1]
        roundKey.append(tmpKey)
    # return
    return roundKey


def DES(_input, _key):
    # preprocess arg (不足64bits 補0)
    if _input[0:2] == "0x":
        _input = bin(int(_input[2:], 16))[2:].zfill(64)
    else:
        _input = _input.zfill(64)
    if _key[0:2] == "0x":
        _key = bin(int(_key[2:], 16))[2:].zfill(64)
    else:
        _key = _key.zfill(64)

    # plaintxt initial permutation (PI)
    input = ""
    for i in PI:
        input = input + _input[i-1]

    # key = key schedule (list of string)
    roundKey = keySchedule(_key)

    # round 16 times encryption
    tmpLeft = input[:32]
    tmpRight = input[32:]
    for i in range(0, 16):
        # call encrypt
        if i != 15:
            (tmpLeft, tmpRight) = encrypt(tmpLeft, tmpRight, roundKey[i])
        else:
            (tmpRight, tmpLeft) = encrypt(tmpLeft, tmpRight, roundKey[i])
    # final permutation(PI_1)
    tmp = tmpLeft+tmpRight
    output = ""
    for i in PI_1:
        output = output + tmp[i-1]

    return output


